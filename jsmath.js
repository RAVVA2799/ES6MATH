//JS MATH FUNCTIONS //

//MATH TRUNCATE FUNCTION RETURNS ONLY THE INTEGER PART AND IGNORES THE DECIMAL PART.

const k=Math.trunc(4.7);
console.log(k);
console.log(Math.trunc(-8.67));

//.............................//
//MATH SIGN WILL RETURN -1,0,1 IF THE NUMBERS ARE NEGATIVE, ZERO OR POSITIVE//

console.log(Math.sign(-4));
console.log(Math.sign(0));
console.log(Math.sign(+56));

//...............................//

//math cbrt will return the cube root of the given number.

